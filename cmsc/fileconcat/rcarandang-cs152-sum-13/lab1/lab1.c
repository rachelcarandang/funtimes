#include <stdio.h>
int main (void) {
	char scale;
	float temp;
	printf ("Enter in a temperature scale ('f' for Fahrenheit or 'c' for Celsius): ");
	scanf ("%c", &scale);
	if (scale=='c') {
		printf("Enter in a temperature: ");
		scanf("%f", &temp);
		printf("%f C = %f F\n", temp, temp * 9 / 5 + 32);
	}
	else if (scale=='f') {
		printf("Enter in a temperature: ");
		scanf("%f", &temp);
		printf("%f F = %f C\n", temp, (temp - 32) * 5 / 9);
	}
	else 
		printf ("Invalid temperature scale\n");	
	return 0;
}




